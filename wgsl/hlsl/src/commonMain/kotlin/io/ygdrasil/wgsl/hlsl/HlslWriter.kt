package io.ygdrasil.wgsl.hlsl

import io.ygdrasil.wgsl.arena.Handle
import io.ygdrasil.wgsl.back.HlslOptions
import io.ygdrasil.wgsl.back.BindingMap
import io.ygdrasil.wgsl.back.WriterBase
import io.ygdrasil.wgsl.back.BackendWriter
import io.ygdrasil.wgsl.ir.*
import io.ygdrasil.wgsl.ir.Function
import io.ygdrasil.wgsl.proc.Layouter
import io.ygdrasil.wgsl.proc.Namer
import io.ygdrasil.wgsl.valid.ModuleInfo

class HlslWriter(
    output: StringBuilder,
    module: Module,
    moduleInfo: ModuleInfo,
    options: HlslOptions,
    namer: Namer,
    layouter: Layouter
) : WriterBase<HlslOptions>(output, module, moduleInfo, options, namer, layouter), BackendWriter<HlslOptions> {

    override fun write(module: Module, moduleInfo: ModuleInfo): String {
        return write()
    }

    override fun withOptions(options: HlslOptions): BackendWriter<HlslOptions> {
        return HlslWriter(StringBuilder(), module, moduleInfo, options, namer, layouter)
    }

    override fun canHandle(module: Module, moduleInfo: ModuleInfo): Boolean = true

    override fun writeHeader() {
        writeLine("// Generated by WebGPU-KTypes HLSL Backend")
    }

    override fun writeStructType(handle: Handle<Type>, structInner: TypeInner.Struct, name: String) {
        writeLine("struct $name {")
        indent {
            for (member in structInner.members) {
                val memberName = member.name
                val typeName = getTypeName(member.type)
                writeLine("$typeName $memberName;")
            }
        }
        writeLine("};")
    }

    override fun writeFunctionSignature(func: Function, name: String) {
        val returnType = func.returnType?.let { getTypeName(it) } ?: "void"
        write("$returnType $name(")
        func.parameters.forEachIndexed { i, param ->
            if (i > 0) write(", ")
            val typeName = getTypeName(param.type)
            write("$typeName ${param.name}")
        }
        write(")")
    }

    override fun writeGlobalVariables() {
        module.globalVariables.forEachWithHandle { handle, variable ->
            val name = getGlobalVariableName(handle)
            val typeName = getTypeName(variable.type)
            val binding = variable.binding
            if (binding != null) {
                val target = options.bindingMap[binding]
                val register = when (variable.storageClass) {
                    StorageClass.Uniform -> "register(b${target?.buffer ?: binding.index})"
                    StorageClass.Storage -> "register(u${target?.buffer ?: binding.index})"
                    // TODO: handle textures and samplers properly
                    else -> "register(t${target?.texture ?: binding.index})"
                }
                writeLine("$typeName $name : $register;")
            } else {
                val init = variable.init?.let { " = ${writeExpression(it)}" } ?: ""
                writeLine("$typeName $name$init;")
            }
        }
    }

    override fun writeEntryPoint(ep: EntryPoint, index: Int) {
        writeLine()
        val inputStructName = writeInputStruct(ep)
        val outputStructName = writeOutputStruct(ep)

        val stageAttr = when (ep.stage) {
            ShaderStage.Vertex -> "[numthreads(1, 1, 1)]" // Default for VS in some contexts, but not needed
            ShaderStage.Fragment -> ""
            ShaderStage.Compute -> "[numthreads(1, 1, 1)]" // Default
        }
        
        if (ep.stage == ShaderStage.Compute) {
            writeLine(stageAttr)
        }

        val func = module.functions[ep.function]
        val returnType = outputStructName ?: (func.returnType?.let { getTypeName(it) } ?: "void")
        
        write("$returnType ${ep.name}(")
        
        val args = mutableListOf<String>()
        if (inputStructName != null) {
            args.add("$inputStructName stage_in")
        }

        // Built-ins that are not in stage_in (if any, like compute built-ins)
        ep.bindings.forEach { attr ->
            if (attr is BindingAttribute.Builtin && ep.stage == ShaderStage.Compute) {
                val hlslSemantic = getHlslSemantic(attr.builtin)
                val typeName = getHlslBuiltinType(attr.builtin)
                val name = attr.builtin.name.lowercase()
                args.add("$typeName $name : $hlslSemantic")
            }
        }

        write(args.joinToString(", "))
        writeLine(") {")
        indent {
            if (outputStructName != null) {
                writeLine("$outputStructName stage_out;")
            }
            writeBlock(func.body)
            if (outputStructName != null) {
                writeLine("return stage_out;")
            }
        }
        writeLine("}")
    }

    protected fun writeInputStruct(ep: EntryPoint): String? {
        val members = mutableListOf<String>()
        
        ep.bindings.forEach { attr ->
            when (attr) {
                is BindingAttribute.Builtin -> {
                    if (ep.stage != ShaderStage.Compute) {
                        val hlslSemantic = getHlslSemantic(attr.builtin)
                        val typeName = getHlslBuiltinType(attr.builtin)
                        val name = attr.builtin.name.lowercase()
                        members.add("$typeName $name : $hlslSemantic;")
                    }
                }
                is BindingAttribute.Location -> {
                    val typeName = "float4" 
                    val name = "loc_${attr.location}"
                    members.add("$typeName $name : TEXCOORD${attr.location};")
                }
                else -> {}
            }
        }

        if (members.isEmpty()) return null

        val structName = "${ep.name}_Input"
        writeLine("struct $structName {")
        indent {
            members.forEach { writeLine(it) }
        }
        writeLine("};")
        return structName
    }

    protected fun writeOutputStruct(ep: EntryPoint): String? {
        if (ep.stage != ShaderStage.Vertex) return null
        
        val structName = "${ep.name}_Output"
        writeLine("struct $structName {")
        indent {
            writeLine("float4 position : SV_Position;")
            // TODO: other outputs (locations)
        }
        writeLine("};")
        return structName
    }

    override fun writeLiteralValue(value: LiteralValue): String {
        return when (value) {
            is LiteralValue.Scalar -> writeScalarValue(value.value)
            is LiteralValue.Vector -> {
                val first = value.components.first()
                val prefix = when (first) {
                    is ScalarValue.F32 -> "float"
                    is ScalarValue.U32 -> "uint"
                    is ScalarValue.I32 -> "int"
                    is ScalarValue.Bool -> "bool"
                    else -> "float"
                }
                "$prefix${value.components.size}(${value.components.joinToString { writeScalarValue(it) }})"
            }
            is LiteralValue.Matrix -> "mat(...)"
        }
    }

    override fun writeSample(
        texture: String,
        sampler: String?,
        coordinate: String,
        level: SampleLevel?,
        depthRef: Handle<Expression>?
    ): String {
        return when (level) {
            is SampleLevel.Zero -> "$texture.SampleLevel($sampler, $coordinate, 0)"
            is SampleLevel.MIPMAP -> {
                val l = writeExpression(level.level)
                "$texture.SampleLevel($sampler, $coordinate, $l)"
            }
            is SampleLevel.AUTOMATIC -> "$texture.Sample($sampler, $coordinate)"
            null -> {
                if (depthRef != null) {
                    val d = writeExpression(depthRef)
                    "$texture.SampleCmp($sampler, $coordinate, $d)"
                } else {
                    "$texture.Sample($sampler, $coordinate)"
                }
            }
        }
    }

    override fun writeTextureQuery(texture: String, query: TextureQueryKind): String {
        val method = when (query) {
            TextureQueryKind.Size -> "GetDimensions()" // Simplified, returns multiple values
            TextureQueryKind.SizeLevel -> "GetDimensions()"
            TextureQueryKind.NumLevels -> "/* unsupported */"
            TextureQueryKind.NumLayers -> "/* unsupported */"
            TextureQueryKind.NumSamples -> "/* unsupported */"
        }
        return "$texture.$method"
    }

    private fun getHlslSemantic(builtin: BuiltinValue): String = when (builtin) {
        BuiltinValue.Position -> "SV_Position"
        BuiltinValue.VertexIndex -> "SV_VertexID"
        BuiltinValue.InstanceIndex -> "SV_InstanceID"
        BuiltinValue.FrontFacing -> "SV_IsFrontFace"
        BuiltinValue.LocalInvocationId -> "SV_GroupThreadID"
        BuiltinValue.LocalInvocationIndex -> "SV_GroupIndex"
        BuiltinValue.GlobalInvocationId -> "SV_DispatchThreadID"
        BuiltinValue.WorkgroupId -> "SV_GroupID"
        BuiltinValue.SampleIndex -> "SV_SampleIndex"
        BuiltinValue.SampleMask -> "SV_Coverage"
        else -> builtin.name.uppercase()
    }

    private fun getHlslBuiltinType(builtin: BuiltinValue): String = when (builtin) {
        BuiltinValue.Position -> "float4"
        BuiltinValue.VertexIndex, BuiltinValue.InstanceIndex, BuiltinValue.SampleIndex -> "uint"
        BuiltinValue.FrontFacing -> "bool"
        BuiltinValue.LocalInvocationId, BuiltinValue.GlobalInvocationId, 
        BuiltinValue.WorkgroupId -> "uint3"
        BuiltinValue.LocalInvocationIndex, BuiltinValue.SampleMask -> "uint"
        else -> "uint"
    }

    override fun getScalarTypeName(scalar: TypeInner.Scalar): String {
        return when (scalar.kind) {
            ScalarKind.Bool -> "bool"
            ScalarKind.Sint -> "int"
            ScalarKind.Uint -> "uint"
            ScalarKind.F32 -> "float"
            ScalarKind.F16 -> "half"
            ScalarKind.F64 -> "double"
            else -> "void"
        }
    }

    override fun getTypeName(handle: Handle<Type>): String {
        val type = module.types[handle]
        return when (val inner = type.inner) {
            is TypeInner.Scalar -> getScalarTypeName(inner)
            is TypeInner.Vector -> {
                val scalarType = module.types[inner.scalar]
                val scalarName = getScalarTypeName(scalarType.inner as TypeInner.Scalar)
                "$scalarName${inner.size.ordinal + 2}"
            }
            is TypeInner.Matrix -> {
                val scalarType = module.types[inner.scalar]
                val scalarName = getScalarTypeName(scalarType.inner as TypeInner.Scalar)
                "$scalarName${inner.columns.ordinal + 2}x${inner.rows.ordinal + 2}"
            }
            is TypeInner.Struct -> "Struct_${handle.index}"
            is TypeInner.Pointer -> {
                val baseName = getTypeName(inner.base)
                // Simplified HLSL pointer mapping
                "$baseName*"
            }
            is TypeInner.Opaque -> {
                when {
                    inner.name == "sampler" -> "SamplerState"
                    inner.name == "comparison_sampler" -> "SamplerComparisonState"
                    inner.name.startsWith("texture") -> {
                        // Very simple mapping for now
                        "Texture2D<float4>"
                    }
                    else -> inner.name
                }
            }
            else -> "void"
        }
    }

    override fun writeRelational(function: RelationalFunction, arguments: List<String>): String {
        return when (function) {
            RelationalFunction.Any -> "any(${arguments.joinToString()})"
            RelationalFunction.All -> "all(${arguments.joinToString()})"
            RelationalFunction.IsNan -> "isnan(${arguments.joinToString()})"
            RelationalFunction.IsInf -> "isinf(${arguments.joinToString()})"
            RelationalFunction.IsFinite -> "isfinite(${arguments.joinToString()})"
            // HLSL doesn't have a direct isnormal, let's use a placeholder
            RelationalFunction.IsNormal -> "/* isnormal unsupported */ true"
            RelationalFunction.SignBit -> "signbit(${arguments.joinToString()})"
        }
    }

    override fun writeAtomic(pointer: String, function: AtomicFunction, arguments: List<String>): String {
        val hlslFunc = when (function) {
            AtomicFunction.Add -> "InterlockedAdd"
            AtomicFunction.Subtract -> "InterlockedAdd" // use negative
            AtomicFunction.And -> "InterlockedAnd"
            AtomicFunction.Or -> "InterlockedOr"
            AtomicFunction.Xor -> "InterlockedXor"
            AtomicFunction.Min -> "InterlockedMin"
            AtomicFunction.Max -> "InterlockedMax"
            AtomicFunction.Exchange -> "InterlockedExchange"
            AtomicFunction.CompSwap -> "InterlockedCompareExchange"
        }
        // HLSL Interlocked functions are statements, not expressions. 
        // This is a major difference with WGSL/MSL.
        return "$hlslFunc($pointer, ${arguments.joinToString()}, /* old_value */)"
    }

    override fun getBuiltinFunctionName(function: BuiltinFunction): String = when (function) {
        BuiltinFunction.Ln -> "log"
        BuiltinFunction.Mix -> "lerp"
        BuiltinFunction.Fract -> "frac"
        BuiltinFunction.Atan2 -> "atan2"
        BuiltinFunction.Dot -> "dot"
        BuiltinFunction.Cross -> "cross"
        BuiltinFunction.Distance -> "distance"
        BuiltinFunction.Length -> "length"
        BuiltinFunction.Normalize -> "normalize"
        BuiltinFunction.Reflect -> "reflect"
        BuiltinFunction.Refract -> "refract"
        BuiltinFunction.Pow -> "pow"
        BuiltinFunction.Exp -> "exp"
        BuiltinFunction.Exp2 -> "exp2"
        BuiltinFunction.Log2 -> "log2"
        BuiltinFunction.Sqrt -> "sqrt"
        BuiltinFunction.InverseSqrt -> "rsqrt"
        BuiltinFunction.Floor -> "floor"
        BuiltinFunction.Ceil -> "ceil"
        BuiltinFunction.Round -> "round"
        BuiltinFunction.Trunc -> "trunc"
        BuiltinFunction.Sin -> "sin"
        BuiltinFunction.Cos -> "cos"
        BuiltinFunction.Tan -> "tan"
        BuiltinFunction.Asin -> "asin"
        BuiltinFunction.Acos -> "acos"
        BuiltinFunction.Atan -> "atan"
        BuiltinFunction.Sinh -> "sinh"
        BuiltinFunction.Cosh -> "cosh"
        BuiltinFunction.Tanh -> "tanh"
        BuiltinFunction.Asinh -> "asinh"
        BuiltinFunction.Acosh -> "acosh"
        BuiltinFunction.Atanh -> "atanh"
        BuiltinFunction.Abs -> "abs"
        BuiltinFunction.Min -> "min"
        BuiltinFunction.Max -> "max"
        BuiltinFunction.Clamp -> "clamp"
        BuiltinFunction.Step -> "step"
        BuiltinFunction.Smoothstep -> "smoothstep"
        BuiltinFunction.Fma -> "mad"
        BuiltinFunction.Determinant -> "determinant"
        else -> super.getBuiltinFunctionName(function)
    }
}
